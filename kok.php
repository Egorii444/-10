<?php
// Многомерный массив с расписанием уроков в школе
$schedule = array(
    "Понедельник" => array("Математика", "Русский язык", "Литература"),
    "Вторник" => array("Физика", "Химия", "Биология"),
    "Среда" => array("География", "История", "Обществознание"),
    "Четверг" => array("Английский язык", "Французский язык", "Немецкий язык"),
    "Пятница" => array("Информатика", "Физкультура", "Музыка")
);

// Вывод расписания на экран с использованием циклов
echo "Расписание уроков в школе:\n";
foreach ($schedule as $day => $lessons) {
    echo "$day:\n";
    foreach ($lessons as $lesson) {
        echo "- $lesson\n";
    }
    echo "\n";
}
?>